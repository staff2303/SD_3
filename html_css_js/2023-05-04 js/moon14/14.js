var num = prompt("1~5 사이값를 입력하세요!");

switch(num) {
    case "1":
        alert("1을 입력하셨습니다");
        break;
    case "2":
        alert("2을 입력하셨습니다");
        break;
    case "3":
        alert("3을 입력하셨습니다");
        break;
    case "4":
        alert("4을 입력하셨습니다");
        break;
    case "5":
        alert("5을 입력하셨습니다");
        break;
    default:
        alert("1 ~ 5 사이값을 입력해주세요");
        break;
}